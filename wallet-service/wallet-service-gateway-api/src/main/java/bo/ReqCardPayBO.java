package bo;

/**
 * 银行卡支付接口的入参
 * Created by ljp on 2017/11/8.
 */
public class ReqCardPayBO extends ReqBaseBO {

    private String oid;         //订单编号
    private String amount;     //订单总金额
    private String ymd;         //订单产生日期
    private Integer moneyType;  //支付币种
    private String tokenId;     //TOKEN编号
    private String userref;     //客户唯一标识

    public String getOid() {
        return oid;
    }

    public void setOid(String oid) {
        this.oid = oid;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getYmd() {
        return ymd;
    }

    public void setYmd(String ymd) {
        this.ymd = ymd;
    }

    public Integer getMoneyType() {
        return moneyType;
    }

    public void setMoneyType(Integer moneyType) {
        this.moneyType = moneyType;
    }

    public String getTokenId() {
        return tokenId;
    }

    public void setTokenId(String tokenId) {
        this.tokenId = tokenId;
    }

    public String getUserref() {
        return userref;
    }

    public void setUserref(String userref) {
        this.userref = userref;
    }
}
