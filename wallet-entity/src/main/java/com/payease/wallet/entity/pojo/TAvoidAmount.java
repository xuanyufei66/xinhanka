package com.payease.wallet.entity.pojo;

import java.io.Serializable;
import java.util.Date;

public class TAvoidAmount implements Serializable {
    private Integer id;

    private String avoidAmount;

    private Date creattime;

    private static final long serialVersionUID = 1L;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getAvoidAmount() {
        return avoidAmount;
    }

    public void setAvoidAmount(String avoidAmount) {
        this.avoidAmount = avoidAmount;
    }

    public Date getCreattime() {
        return creattime;
    }

    public void setCreattime(Date creattime) {
        this.creattime = creattime;
    }
}